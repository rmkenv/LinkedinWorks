def parse_station_gdf(df, source_label):
    lat_col = next((c for c in df.columns if 'lat' in c.lower()), None)
    lon_col = next((c for c in df.columns if 'lon' in c.lower() or 'lng' in c.lower()), None)
    if lat_col is None and 'the_geom' in df.columns:
        df['longitude'] = df['the_geom'].apply(lambda g: g['coordinates'][0] if isinstance(g, dict) else None)
        df['latitude']  = df['the_geom'].apply(lambda g: g['coordinates'][1] if isinstance(g, dict) else None)
        lat_col, lon_col = 'latitude', 'longitude'
    elif lat_col is None and 'location' in df.columns:
        df['latitude']  = df['location'].apply(lambda x: float(x['latitude'])  if isinstance(x, dict) else None)
        df['longitude'] = df['location'].apply(lambda x: float(x['longitude']) if isinstance(x, dict) else None)
        lat_col, lon_col = 'latitude', 'longitude'
    df[lat_col] = pd.to_numeric(df[lat_col], errors='coerce')
    df[lon_col] = pd.to_numeric(df[lon_col], errors='coerce')
    df = df.dropna(subset=[lat_col, lon_col])
    name_col = next((c for c in df.columns if any(k in c.lower() for k in ['name','facilityname','facname','unit_id'])), None)
    df['station_name'] = df[name_col].astype(str) if name_col else f'{source_label} Station'
    df['source'] = source_label
    return gpd.GeoDataFrame(df, geometry=gpd.points_from_xy(df[lon_col], df[lat_col]), crs='EPSG:4326')

