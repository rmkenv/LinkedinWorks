def save_raster_to_tif(da, filepath, crs_epsg=32618):
    """Save an xarray DataArray to GeoTIFF using rioxarray."""
    da_out = da.copy()
    # Ensure spatial_ref / CRS is attached
    if not da_out.rio.crs:
        da_out = da_out.rio.write_crs(f'EPSG:{crs_epsg}')
    da_out = da_out.rio.write_nodata(np.nan)
    da_out.rio.to_raster(filepath, driver='GTiff', dtype='float32')
    print(f'  Saved {filepath} ({os.path.getsize(filepath)/1e6:.1f} MB)')

print('Saving rasters...')
