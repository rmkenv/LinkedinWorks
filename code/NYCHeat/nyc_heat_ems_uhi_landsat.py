"""## 2. Land Surface Temperature via Planetary Computer (ARSET Methodology)

### Background
Following the NASA ARSET UHI training, we use **Landsat Collection 2 Level-2** thermal data:
- **Band:** `lwir11` (Band 10 on L8/L9) — Long-wave infrared, 100m resampled to 30m
- **Product:** Surface temperature, already atmospherically corrected by USGS
- **Conversion:** `LST_Kelvin = DN × 0.00341802 + 149.0` (USGS Collection 2 scale/offset)
- **Collection:** `landsat-c2-l2` on Planetary Computer (Landsat 4–9 unified)

### Access
Planetary Computer uses **Azure SAS tokens** for data access — the `planetary-computer` SDK handles this automatically. No account needed for anonymous access; an optional free API key lifts rate limits.

### Scene selection
- Summers 2022–2024 (June 1 – September 30 each year)
- Cloud cover < 20%
- Landsat 8 or 9 (OLI-TIRS sensor)
- NYC bounding box
- We stack all qualifying scenes and take the **mean** to get a stable summer LST composite
"""

# NYC bounding box (WGS84)
NYC_BBOX = [-74.26, 40.49, -73.69, 40.93]  # [west, south, east, north]

# Connect to Planetary Computer STAC API (anonymous, no key needed)
catalog = pystac_client.Client.open(
    'https://planetarycomputer.microsoft.com/api/stac/v1',
    modifier=planetary_computer.sign_inplace  # auto-signs asset URLs with SAS tokens
)
print('Connected to Planetary Computer STAC API')

# Search for Landsat C2 L2 scenes over NYC for 3 summers
# We search each year separately then combine to ensure even seasonal coverage
all_items = []
for year in [2022, 2023, 2024]:
    search = catalog.search(
        collections=['landsat-c2-l2'],
        bbox=NYC_BBOX,
        datetime=f'{year}-06-01/{year}-09-30',
        query={
            'eo:cloud_cover': {'lt': 20},
            'platform': {'in': ['landsat-8', 'landsat-9']}
        }
    )
    items = list(search.items())
    print(f'  {year}: {len(items)} scenes (cloud < 20%, L8/L9)')
    all_items.extend(items)

print(f'\nTotal scenes: {len(all_items)}')

# Preview metadata of first scene
if all_items:
    sample = all_items[0]
    print(f'\nSample scene: {sample.id}')
    print(f'  Date: {sample.datetime}')
    print(f'  Platform: {sample.properties.get("platform")}')
    print(f'  Cloud cover: {sample.properties.get("eo:cloud_cover")}%')
    print(f'  Available bands: {list(sample.assets.keys())[:15]}')

# Load the lwir11 (thermal) band for all scenes into an xarray DataArray
# odc.stac.load handles the SAS token signing, COG streaming, and reprojection

print('Loading lwir11 (thermal infrared) band from Planetary Computer...')
print('(Streaming COGs from Azure Blob Storage — may take 2-5 minutes for 3 summers of scenes)')

# Use UTM 18N (EPSG:32618) — appropriate projection for NYC at 30m
# odc-stac will reproject all scenes to this CRS on-the-fly
ds_lst = odc.stac.load(
    all_items,
    bbox=NYC_BBOX,
    bands=['lwir11'],
    crs='EPSG:32618',
    resolution=30,
    groupby='solar_day',    # one composite per unique overpass date
    chunks={'time': 1, 'x': 512, 'y': 512}  # lazy-load with dask chunks
)

print(f'Dataset loaded: {ds_lst}')
print(f'\nDimensions: {dict(ds_lst.dims)}')
print(f'Time steps (unique overpasses): {len(ds_lst.time)}')

# ── ARSET LST Conversion ──
# Landsat C2 L2 surface temperature is stored as scaled integers.
# USGS scale: LST (Kelvin) = DN × 0.00341802 + 149.0
# Reference: USGS Landsat Collection 2 Level-2 Science Product Guide

SCALE  = 0.00341802
OFFSET = 149.0

print('Converting DN → Kelvin → Fahrenheit...')

lwir = ds_lst['lwir11'].astype('float32')

# Mask fill/nodata values (0 and 65535 are common fill values in C2 L2)
lwir = lwir.where((lwir > 0) & (lwir < 65535))

# DN → Kelvin
lst_k = lwir * SCALE + OFFSET

# Kelvin → Fahrenheit  (ARSET training uses Fahrenheit for US audiences)
lst_f = (lst_k - 273.15) * 9/5 + 32

# Sanity check: NYC summer LST should be roughly 60°F–130°F
lst_f_computed = lst_f.compute()  # trigger dask computation
print(f'  LST range: {float(lst_f_computed.min()):.1f}°F – {float(lst_f_computed.max()):.1f}°F')
print(f'  Mean LST: {float(lst_f_computed.mean()):.1f}°F')
print(f'  Scenes with valid data: {int((~np.isnan(lst_f_computed)).any(dim=["x","y"]).sum())}')

# ── Build mean summer LST composite ──
# Mean across all overpasses = stable seasonal signal
# This is the ARSET approach: temporal composite to reduce cloud/noise artifacts

print('Building mean summer LST composite across all overpasses...')
lst_mean = lst_f_computed.mean(dim='time', skipna=True)

print(f'  Composite LST range: {float(lst_mean.min()):.1f}°F – {float(lst_mean.max()):.1f}°F')
print(f'  Composite LST mean:  {float(lst_mean.mean()):.1f}°F')
print(f'  Output shape: {lst_mean.shape} (y × x pixels at 30m resolution)')

# ── Z-score the composite to get UHI intensity ──
# Z-score tells us how many standard deviations above/below city-wide mean each pixel is
# Positive z = warmer = UHI hotspot; Negative z = cooler = park/water/green space
lst_global_mean = float(lst_mean.mean())
lst_global_std  = float(lst_mean.std())

lst_zscore = (lst_mean - lst_global_mean) / lst_global_std

print(f'\nUHI z-score range: {float(lst_zscore.min()):.2f} – {float(lst_zscore.max()):.2f}')
print(f'  Global mean LST: {lst_global_mean:.1f}°F')
print(f'  Global std LST:  {lst_global_std:.1f}°F')

# ── Quick diagnostic plot ──
fig, axes = plt.subplots(1, 2, figsize=(14, 5))

lst_mean.plot(
    ax=axes[0],
    cmap='inferno',
    robust=True,
    cbar_kwargs={'label': 'LST (°F)'}
)
axes[0].set_title('Mean Summer LST (Landsat C2 L2, 2022–2024)')
axes[0].set_aspect('equal')

lst_zscore.plot(
    ax=axes[1],
    cmap='RdBu_r',
    robust=True,
    vmin=-3, vmax=3,
    cbar_kwargs={'label': 'UHI z-score'}
)
axes[1].set_title('UHI Intensity (z-score, warmer = redder)')
axes[1].set_aspect('equal')

plt.tight_layout()
plt.savefig('nyc_lst_composite.png', dpi=120, bbox_inches='tight')
plt.show()
print('Saved nyc_lst_composite.png')

